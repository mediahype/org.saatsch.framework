package de.osrg.base.expressions;

public class ExpressionsException extends Exception {

  private static final long serialVersionUID = -7160668720827958856L;

  public ExpressionsException() {
    super();
  }

  public ExpressionsException(String message, Throwable cause, boolean enableSuppression,
      boolean writableStackTrace) {
    super(message, cause, enableSuppression, writableStackTrace);
  }

  public ExpressionsException(String message, Throwable cause) {
    super(message, cause);
  }

  public ExpressionsException(String message) {
    super(message);
  }

  public ExpressionsException(Throwable cause) {
    super(cause);
  }

  
  
}
